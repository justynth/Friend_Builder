package com.cse442.friend_builder.model;

import java.util.ArrayList;

public abstract class User {
    protected String email;
    protected String name;
    protected String description;
    protected double longitude;
    protected double latitude;
    protected String interest0;
    protected String interest1;
    protected String interest2;



    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() { return description; }

    public void setDescription(String description) { this.description = description; }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public String getInterest0() {
        return interest0;
    }

    public void setInterest0(String interest0) {
        this.interest0 = interest0;
    }

    public String getInterest1() {
        return interest1;
    }

    public void setInterest1(String interest1) {
        this.interest1 = interest1;
    }

    public String getInterest2() {
        return interest2;
    }

    public void setInterest2(String interest2) {
        this.interest2 = interest2;
    }
}
